package com.bikepnp;

public class HorizontalScrollItem {
    private int image;
    private String title;
    public HorizontalScrollItem(int image, String title) {
        this.image = image;
        this.title = title;
    }
    public int getImage() {
        return image;
    }
    public String getTitle() {
        return title;
    }
}
